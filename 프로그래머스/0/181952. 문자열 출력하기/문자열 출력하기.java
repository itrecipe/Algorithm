import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in); //스캐너 클래스 사용 스캐너 객체 생성하여 sc변수에 대입하여 사용
        String a = sc.next(); //next로 "HelloWorld!" 문자열을 입력하여 변수 a에 대입
        System.out.println(a); //출력문으로 a찍어보면 원하는 결과값이 나온다.
    }
}